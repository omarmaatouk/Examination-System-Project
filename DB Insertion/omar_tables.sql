-- ===============================================
-- Script: Insert 1000 students into Stud_get_certificate
-- Distributing them evenly over ~240 certificates
-- ===============================================

DECLARE @TotalStudents INT = 1000;
DECLARE @TotalCertificates INT = 240;
DECLARE @CurrentStudentID INT = 1;
DECLARE @CurrentCertificateID INT = 1;

WHILE @CurrentStudentID <= @TotalStudents
BEGIN
    INSERT INTO Stud_get_certificate (St_ID, Cer_ID)
    VALUES (@CurrentStudentID, @CurrentCertificateID);

    SET @CurrentStudentID += 1;
    SET @CurrentCertificateID += 1;

    -- Loop back to 1 if exceeded number of certificates
    IF @CurrentCertificateID > @TotalCertificates
        SET @CurrentCertificateID = 1;
END

